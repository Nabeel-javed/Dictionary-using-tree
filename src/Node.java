
public class Node {
	
	Dictionary data;
	Node left;
	Node right;
	int height;
	
	
	
	public Dictionary getData() {
		return data;
	}
	public void setData(Dictionary data) {
		this.data = data;
	}
	public Node getLeft() {
		return left;
	}
	public void setLeft(Node left) {
		this.left = left;
	}
	
	public Node getRight() {
		return right;
	}
	public void setRight(Node right) {
		this.right = right;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}


}

